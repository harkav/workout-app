class Exercise: 


    def __init__(self, name, reps, weight_in_kgs): 
        self._name = name 
        self._reps = reps 
        self._weight_in_kgs = weight_in_kgs


    def get_name(self): 
        return self._name 

    def get_reps(self): 
        return self._reps 

    def get_weight_in_kgs(self): 
        return self._weight_in_kgs 


    